            $(this).on('click', function(){
                $('.quecat').find('a').removeClass('fselected');
                $(this).addClass('fselected');
                var ccat = $(this).attr('data-cat');
                if(ccat == "all"){
                    $('.faqcatwise').find('li').show();
                }else{
                    $('.faqcatwise').find('li').hide();
                    $('.faqcatwise').find('li[data-faq='+ccat+']').show();
                }
            });
        });
    });
